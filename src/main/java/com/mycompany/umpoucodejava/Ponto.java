

package com.mycompany.umpoucodejava;


public class Ponto {
    
    private Integer x;
    private Integer y;

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public void setY(Integer y) {
        this.y = y;
    }
    
    /**
     *
     */
    public Ponto()
    {
        this.x = 0;
        this.y = 0;
        
    }
    
    public Ponto(Integer x, Integer y)
    {
        this.x = x;
        this.y = y;
        
    }
    
 
}
    

    

